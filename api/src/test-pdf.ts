async function testPdfGeneration() {
  console.log('='.repeat(60));
  console.log('PDF Generation Test');
  console.log('='.repeat(60));
  console.log('');

  try {
    // Initialize PDF generator
    console.log('[1/4] Initializing PDF generator...');
    const pdfGenerator = getPdfGenerator();

    // Generate PDF from HTML
    console.log('[2/4] Generating PDF from sample HTML...');
    const pdfBuffer = await pdfGenerator.generatePdf(SAMPLE_HTML, {
      format: 'Letter',
      printBackground: true,
    });
    console.log(`‚úÖ PDF generated (${pdfBuffer.length} bytes)`);

    // Initialize storage client
    console.log('[3/4] Storing PDF to local filesystem...');
    const storageClient = new LocalStorageClient('./tmp');
    const timestamp = Date.now();
    const fileName = `test-report-${timestamp}.pdf`;
    const filePath = await storageClient.storePdf(fileName, pdfBuffer);
    console.log(`‚úÖ PDF stored at: ${filePath}`);

    // Verify file exists
    console.log('[4/4] Verifying stored file...');
    const exists = await storageClient.exists(fileName);
    if (exists) {
      const publicUrl = await storageClient.getPublicUrl(fileName);
      console.log(`‚úÖ File verified`);
      console.log(`üìÑ Access URL: ${publicUrl}`);
    } else {
      console.log('‚ùå File not found after storage');
    }

    console.log('');
    console.log('='.repeat(60));
    console.log('‚úÖ PDF Generation Test Complete');
    console.log('='.repeat(60));
    console.log('');
    console.log(`File location: ${filePath}`);
    console.log(`Open the file to view the generated PDF`);

  } catch (error) {
    console.error('');
    console.error('='.repeat(60));
    console.error('‚ùå Test Failed');
    console.error('='.repeat(60));
    console.error(error);
    process.exit(1);
  } finally {
    // Cleanup browser
    await closePdfGenerator();
  }
}

// Run the test
testPdfGeneration();
